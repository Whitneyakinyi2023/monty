/**
 * struct temp_stack - holder to a variable
 * @connect: file that is likned to a stream from a file
 * @command: (string) line of text to read from
 * Description: this funtion acts as a variable holder
 */
typedef struct temp_stack
{
        FILE *connect;
	char *command;
} arg_s;

extern arg_s *value;
/**
 * struct stack_s - doubly linked list representation of a stack (or queue)
 * @n: integer
 * @prev: points to the previous element of the stack (or queue)
 * @next: points to the next element of the stack (or queue)
 *
 * Description: doubly linked list node structure
 * for stack, queues, LIFO, FIFO
 */
typedef struct stack_s
{
        int n;
        struct stack_s *prev;
        struct stack_s *next;
} stack_t;

/**
 * struct instruction_s - opcode and its function
 * @opcode: the opcode
 * @f: function to handle the opcode
 *
 * Description: opcode and its function
 * for stack, queues, LIFO, FIFO
 */
typedef struct instruction_s
{
        char *opcode;
        void (*f)(stack_t **stack, unsigned int line_number);
} instruction_t;

void verify_c(int argc);
void error_malloc(void);
void free_value(int argc, char *argv[]);

#endif /* MONTY_H */
